static struct {
    int inited;
    int playing;
    int paused;
    int looping;

    const uint8_t *data;
    uint32_t len;

    uint16_t format;
    uint16_t num_tracks;
    uint16_t division;

    uint32_t us_per_beat;

    midi_track_t tracks[MIDI_MAX_TRACKS];

    uint32_t last_pump_us;

    /* Per-MIDI-channel state */
    uint8_t ch_program[16];
    uint8_t ch_volume[16];
    uint8_t ch_pan[16];
    int16_t ch_bend[16];

    /* Per-OPL-channel state */
    struct {
        int8_t  note;
        int8_t  midi_ch;
        int8_t  velocity;
        /* partner >= 0 → another OPL channel that's the second half of
         * this note's voice stack (pseudo-4-op patches play voice 1
         * and voice 2 on independent channels). On key-off / steal /
         * all-notes-off both channels are released together. -1 = no
         * partner (plain 2-op voice). */
        int8_t  partner;
        /* Patch operator base offset: 4 = voice 1 (uses inst[4..13],
         * note_offset1, fb_cnt1), 14 = voice 2 (uses inst[14..23],
         * note_offset2, fb_cnt2). Lets update_channel_pitch and
         * update_channel_volume target the right operators on each
         * half of a stacked patch without re-deriving v1/v2. */
        int8_t  op_base;
        uint32_t age;
    } opl[OPL_CHANNELS];
    const uint8_t *opl_inst[OPL_CHANNELS]; /* loaded instrument pointer */

    uint32_t age_counter;
    int master_volume;
    const uint8_t *bank;
} M;

/* ======================================================================
 * Helpers
 * ====================================================================== */

static uint16_t rd16(const uint8_t *p) {
    return ((uint16_t)p[0] << 8) | p[1];
}

static uint32_t rd32(const uint8_t *p) {
    return ((uint32_t)p[0] << 24) | ((uint32_t)p[1] << 16) |
           ((uint32_t)p[2] << 8) | p[3];
}

static uint32_t read_var(midi_track_t *t) {
    uint32_t val = 0;
    const uint8_t *d = t->data;
    while (t->pos < t->len) {
        uint8_t b = d[t->pos++];
        val = (val << 7) | (b & 0x7F);
        if (!(b & 0x80)) break;
    }
    return val;
}

/* ======================================================================
 * Patch accessors — 25-byte records from the WOPL-derived bank.
 * ====================================================================== */

static const uint8_t *get_instrument(int program) {
    if (program < 0) program = 0;
    if (program > 127) program = 127;
    const uint8_t *b = M.bank ? M.bank : gm_bank4;
    return b + program * OF_MIDI_INST4_SIZE;
}

static const uint8_t *get_drum(int note) {
    if (note < 35 || note > 81) note = 38; /* default to snare */
    /* User bank layout mirrors the built-in: 128 melodic entries then
     * 47 drums (index 128..174). */
    const uint8_t *b = M.bank ? M.bank + 128 * OF_MIDI_INST4_SIZE : gm_bank4_drum;
    return b + (note - 35) * OF_MIDI_INST4_SIZE;
}

/* Pseudo-4-op = stacked 2-op voices (DMXOPL/DMX style). NOT real OPL3
 * 4-op chain mode — these patches must be loaded as TWO independent
 * 2-op voices on separate channels with their own note offsets. */
static int inst_is_pseudo4op(const uint8_t *p) { return p[0] & 0x01; }
static int inst_is_blank(const uint8_t *p)     { return p[0] & 0x02; }
/* WOPL note_offset1/2: per-voice semitone shift applied to the MIDI
 * note before frequency lookup. Bass / piano / trumpet patches use +12
 * to compensate for their internal MULT=0.5 tuning, and pseudo-4-op
 * stacks may use different offsets per voice for octave doubling. */
static int inst_note_offset(const uint8_t *p)  { return (int8_t)p[1]; }
static int inst_note_offset2(const uint8_t *p) { return (int8_t)p[25]; }
/* percussion_key_number: when non-zero for a drum patch, overrides the
 * GM drum map and plays at this fixed MIDI note. */
static int inst_percussion_key(const uint8_t *p) { return p[24]; }

/* ======================================================================
 * OPL3 driver — 2-op and 4-op patch load paths.
 *
 * Operator layout in a 25-byte record:
 *   op1 (pair1 modulator) @ [4..8]
 *   op2 (pair1 carrier)   @ [9..13]
 *   op3 (pair2 modulator) @ [14..18]  (4-op only)
 *   op4 (pair2 carrier)   @ [19..23]  (4-op only)
 *
 * For 2-op mode we ignore ops 3-4 and write the patch to a single
 * OPL3 channel. For 4-op mode ops 1-2 go on the primary channel's
 * operators (mod,car) and ops 3-4 on the secondary channel's, with
 * the 4-op bit set in register 0x104.
 * ====================================================================== */

/* Load a 2-op patch onto an OPL3 channel from an arbitrary base offset
 * inside the 26-byte record. Voice 1 of any patch is at op_base=4 with
 * fb_cnt at p[2]; voice 2 of a pseudo-4-op patch is at op_base=14 with
 * fb_cnt at p[3]. The carrier KSL_TL byte (op_base+6) is intentionally
 * skipped here — apply_velocity_2op writes the velocity-scaled value. */
static void load_instrument_2op_at(int ch, const uint8_t *p,
                                   int op_base, uint8_t fb_cnt) {
    /* Pair this channel might belong to must NOT be in 4-op mode. */
    fourop_disable(ch);

    /* op1 = modulator (write all 5 operator regs as-is) */
    opl_op_write(ch, 0, 0x20, p[op_base + 0]);
    opl_op_write(ch, 0, 0x40, p[op_base + 1]);
    opl_op_write(ch, 0, 0x60, p[op_base + 2]);
    opl_op_write(ch, 0, 0x80, p[op_base + 3]);
    opl_op_write(ch, 0, 0xE0, p[op_base + 4]);

    /* op2 = carrier (KSL_TL@0x40 deliberately not written; per-note
     * velocity scaling handles it). */
    opl_op_write(ch, 1, 0x20, p[op_base + 5]);
    opl_op_write(ch, 1, 0x60, p[op_base + 7]);
    opl_op_write(ch, 1, 0x80, p[op_base + 8]);
    opl_op_write(ch, 1, 0xE0, p[op_base + 9]);

    /* FB/CNT (L+R output always enabled here; apply_pan can refine
     * the L/R bits later when CC10 / CC pan is applied). */
    opl_ch_write(ch, 0xC0, (fb_cnt & 0x0F) | 0x30);

    M.opl_inst[ch] = p;
}

static void load_instrument_2op(int ch, const uint8_t *p) {
    load_instrument_2op_at(ch, p, 4, p[2]);
}

/* Real OPL3 4-op chain mode loader. Currently unused — DMXOPL3-GS has
 * no true 4-op patches; left in place for future banks that do. */
__attribute__((unused))
static void load_instrument_4op(int prim, int sec, const uint8_t *p) {
    if (M.opl_inst[prim] == p && M.opl_inst[sec] == p)
        return;

    /* Mark this pair as a single 4-op voice BEFORE writing operators:
     * the chip uses the mode bit to route op3/op4 correctly. */
    fourop_enable(prim);

    /* op1 = mod of pair 1 → primary modulator */
    opl_op_write(prim, 0, 0x20, p[4]);
    opl_op_write(prim, 0, 0x40, p[5]);
    opl_op_write(prim, 0, 0x60, p[6]);
    opl_op_write(prim, 0, 0x80, p[7]);
    opl_op_write(prim, 0, 0xE0, p[8]);

    /* op2 = car of pair 1 → primary carrier (TL overridden at note-on). */
    opl_op_write(prim, 1, 0x20, p[9]);
    opl_op_write(prim, 1, 0x60, p[11]);
    opl_op_write(prim, 1, 0x80, p[12]);
    opl_op_write(prim, 1, 0xE0, p[13]);

    /* op3 = mod of pair 2 → secondary modulator */
    opl_op_write(sec, 0, 0x20, p[14]);
    opl_op_write(sec, 0, 0x40, p[15]);
    opl_op_write(sec, 0, 0x60, p[16]);
    opl_op_write(sec, 0, 0x80, p[17]);
    opl_op_write(sec, 0, 0xE0, p[18]);

    /* op4 = car of pair 2 → secondary carrier (TL overridden at note-on). */
    opl_op_write(sec, 1, 0x20, p[19]);
    opl_op_write(sec, 1, 0x60, p[21]);
    opl_op_write(sec, 1, 0x80, p[22]);
    opl_op_write(sec, 1, 0xE0, p[23]);

    /* FB/CNT bytes: primary holds FB and CNT1; secondary holds CNT2.
     * Both channels' C0 registers must have L+R output bits set. */
    opl_ch_write(prim, 0xC0, (p[2] & 0x0F) | 0x30);
    opl_ch_write(sec,  0xC0, (p[3] & 0x0F) | 0x30);

    M.opl_inst[prim] = p;
    M.opl_inst[sec]  = p;
}

/* DMX velocity→amplitude curve (from chocolate-doom i_oplmusic.c).
 * Log-ish mapping used by both velocity and channel volume lookups. */
static const uint8_t dmx_volume_map[128] = {
      0,   1,   3,   5,   6,   8,  10,  11,
     13,  14,  16,  17,  19,  20,  22,  23,
     25,  26,  27,  29,  30,  32,  33,  34,
     36,  37,  39,  41,  43,  45,  47,  49,
     50,  52,  54,  55,  57,  59,  60,  61,
     63,  64,  66,  67,  68,  69,  71,  72,
     73,  74,  75,  76,  77,  79,  80,  81,
     82,  83,  84,  84,  85,  86,  87,  88,
     89,  90,  91,  92,  92,  93,  94,  95,
     96,  96,  97,  98,  99,  99, 100, 101,
    101, 102, 103, 103, 104, 105, 105, 106,
    107, 107, 108, 109, 109, 110, 110, 111,
    112, 112, 113, 113, 114, 114, 115, 115,
    116, 117, 117, 118, 118, 119, 119, 120,
    120, 121, 121, 122, 122, 123, 123, 123,
    124, 124, 125, 125, 126, 126, 127, 127,
};

/* Compute velocity-scaled KSL/TL register value for one operator.
 * base_ksl_tl is the patch byte: high two bits are KSL (preserved),
 * low six bits are base TL attenuation (adds to the velocity attenuation). */
static uint8_t dmx_tl_byte(uint8_t base_ksl_tl, int velocity, int ch_vol) {
    int base_tl = base_ksl_tl & 0x3F;
    int v  = velocity & 0x7F;
    int cv = ch_vol   & 0x7F;
    cv = (cv * M.master_volume) / 255;
    if (cv > 127) cv = 127;
    unsigned midi_volume = 2u * (dmx_volume_map[cv] + 1u);
    unsigned full_volume = (dmx_volume_map[v] * midi_volume) >> 9;
    int tl_add = 63 - (int)full_volume;
    int tl = base_tl + tl_add;
    if (tl > 63) tl = 63;
    return (uint8_t)((base_ksl_tl & 0xC0) | tl);
}

/* Apply per-note velocity/volume scaling to the operators that actually
 * produce output, picked from the CNT1/CNT2 topology bits:
 *   2-op CNT=0 (FM)  → op2 only
 *   2-op CNT=1 (AM)  → op1 + op2
 *   4-op 0,0 (chain) → op4 only
 *   4-op 0,1 (2+2)   → op2 + op4
 *   4-op 1,0 (1+3c)  → op1 + op4
 *   4-op 1,1 (parallel) → op1 + op3 + op4
 * Scaling a modulator would change FM timbre, so we only touch the
 * operators that feed the chip output in the given topology. */
/* Apply velocity scaling to one 2-op voice on a single OPL3 channel.
 * mod_kt and car_kt are the patch's KSL_TL bytes for the voice's
 * modulator/carrier; cnt is the connection bit (0 = FM, op2 carrier;
 * 1 = AM, both carriers). */
static void apply_velocity_2op(int ch, uint8_t mod_kt, uint8_t car_kt,
                               int cnt, int velocity, int ch_vol) {
    if (cnt)
        opl_op_write(ch, 0, 0x40, dmx_tl_byte(mod_kt, velocity, ch_vol));
    opl_op_write(ch, 1, 0x40, dmx_tl_byte(car_kt, velocity, ch_vol));
}

static void apply_velocity(int prim, int sec, const uint8_t *p,
                           int velocity, int ch_vol, int is_4op) {
    int cnt1 = p[2] & 0x01;
    int cnt2 = p[3] & 0x01;

    if (!is_4op) {
        apply_velocity_2op(prim, p[5], p[10], cnt1, velocity, ch_vol);
        return;
    }

    /* True OPL3 4-op chain mode (rarely used by current banks). op4 is
     * the final carrier in every topology except the (unusual)
     * all-parallel case — always scale it. */
    opl_op_write(sec, 1, 0x40, dmx_tl_byte(p[20], velocity, ch_vol));

    if (cnt1 == 0 && cnt2 == 1) {
        opl_op_write(prim, 1, 0x40, dmx_tl_byte(p[10], velocity, ch_vol));
    } else if (cnt1 == 1 && cnt2 == 0) {
        opl_op_write(prim, 0, 0x40, dmx_tl_byte(p[5],  velocity, ch_vol));
    } else if (cnt1 == 1 && cnt2 == 1) {
        opl_op_write(prim, 0, 0x40, dmx_tl_byte(p[5],  velocity, ch_vol));
        opl_op_write(sec,  0, 0x40, dmx_tl_byte(p[15], velocity, ch_vol));
    }
}

/* Apply pan (CC10) to OPL3 C0 register L/R bits. For 4-op voices,
 * update BOTH channels in the pair so both operator outputs route to
 * the same side. */
static void apply_pan_ch(int ch, int midi_ch) {
    const uint8_t *p = M.opl_inst[ch];
    if (!p) return;
    /* Use the primary fb_cnt byte for the primary channel, secondary
     * fb_cnt byte for the secondary channel. */
    int is_sec = (M.opl[ch].partner >= 0 && ch != M.opl[M.opl[ch].partner].partner);
    (void)is_sec;  /* simplified: use p[2] for primary, p[3] for secondary */
    uint8_t fb_cnt = p[2] & 0x0F;
    /* Distinguish primary vs secondary: look at ch_to_4op_pair — if
     * this channel equals opl_4op_sec[pair], use p[3]. */
    int pair = ch_to_4op_pair(ch);
    if (pair >= 0 && opl_4op_sec[pair] == ch)
        fb_cnt = p[3] & 0x0F;
    int pan = M.ch_pan[midi_ch];
    uint8_t lr;
    if (pan < 43)       lr = 0x10; /* left only */
    else if (pan > 84)  lr = 0x20; /* right only */
    else                lr = 0x30; /* both */
    opl_ch_write(ch, 0xC0, fb_cnt | lr);
}

static void apply_pan(int prim, int midi_ch) {
    apply_pan_ch(prim, midi_ch);
    int partner = M.opl[prim].partner;
    if (partner >= 0)
        apply_pan_ch(partner, midi_ch);
}

static void note_to_freq(int note, int16_t bend, uint8_t *flo, uint8_t *fhi_blk) {
    if (note < 0) note = 0;
    if (note > 127) note = 127;

    int oct = note / 12;
    int semi = note % 12;
    int block = oct - 1;
    if (block < 0) block = 0;
    if (block > 7) block = 7;

    uint32_t fnum = fnums[semi];

    /* Apply pitch bend: ±2 semitones.
     * Linear approximation: factor = 1024 + bend * 125 / 8192 */
    if (bend != 0) {
        int factor = 1024 + (int32_t)bend * 125 / 8192;
        fnum = fnum * (uint32_t)factor / 1024;
        /* Handle overflow into next block */
        while (fnum > 1023 && block < 7) {
            fnum >>= 1;
            block++;
        }
        if (fnum > 1023) fnum = 1023;
    }

    *flo = fnum & 0xFF;
    *fhi_blk = (uint8_t)((block << 2) | ((fnum >> 8) & 0x03));
}

/* ======================================================================
 * Channel allocation
 *
 * The 18 OPL3 channels split into:
 *   - 6 "4-op pairs" (primary + secondary channels that can fuse into
 *     a single 4-op voice via register 0x104)
 *   - 6 standalone 2-op channels (ch 6, 7, 8, 15, 16, 17 — never part
 *     of a pair, always available as 2-op voices)
 *
 * Allocation strategy:
 *   - 4-op patch: pick a free 4-op pair; on exhaustion steal the
 *     oldest 4-op pair (keyoff both halves).
 *   - 2-op patch: pick a free standalone 2-op channel first. On
 *     exhaustion degrade to using one half of an unused 4-op pair
 *     (the pair's 4-op bit gets cleared when load_instrument_2op
 *     calls fourop_disable), else steal the oldest 2-op voice.
 *
 * Returns: primary channel index (for 4-op, sec stored via out-param;
 * for 2-op, sec_out receives -1). -1 on failure. Caller must set
 * M.opl[prim].partner = sec and M.opl[sec].partner = prim for 4-op.
 * ====================================================================== */

static void kill_channel(int ch) {
    int partner = M.opl[ch].partner;
    opl_ch_write(ch, 0xB0, 0x00);
    M.opl[ch].note = -1;
    M.opl[ch].partner = -1;
    M.opl[ch].op_base = 4;
    if (partner >= 0) {
        opl_ch_write(partner, 0xB0, 0x00);
        M.opl[partner].note = -1;
        M.opl[partner].partner = -1;
        M.opl[partner].op_base = 4;
    }
}

/* Free check: both halves of a pair must be inactive for 4-op use. */
static int pair_free(int pair_idx) {
    int p = opl_4op_prim[pair_idx];
    int s = opl_4op_sec [pair_idx];
    return M.opl[p].note < 0 && M.opl[s].note < 0;
}

/* Allocate a fused 4-op channel pair (real OPL3 4-op mode). Currently
 * unused — kept for future banks that ship real-4-op patches. */
__attribute__((unused))
static int alloc_4op(int midi_ch, int *sec_out) {
    (void)midi_ch;
    for (int i = 0; i < OPL_4OP_PAIRS; i++) {
        if (pair_free(i)) {
            *sec_out = opl_4op_sec[i];
            return opl_4op_prim[i];
        }
    }
    /* Steal the oldest pair — age is tracked on the primary. */
    int victim = 0;
    uint32_t victim_age = M.opl[opl_4op_prim[0]].age;
    for (int i = 1; i < OPL_4OP_PAIRS; i++) {
        uint32_t age = M.opl[opl_4op_prim[i]].age;
        if (age < victim_age) { victim = i; victim_age = age; }
    }
    kill_channel(opl_4op_prim[victim]);  /* also kills secondary via partner */
    *sec_out = opl_4op_sec[victim];
    return opl_4op_prim[victim];
}

static int alloc_2op(int midi_ch) {
    (void)midi_ch;
    /* Prefer standalone 2-op channels first. */
    for (int i = 0; i < OPL_2OP_ONLY_COUNT; i++) {
        int ch = opl_2op_only[i];
        if (M.opl[ch].note < 0)
            return ch;
    }
    /* Fall back to a free 4-op pair half — using it as 2-op will
     * clear the pair's 4-op bit. Prefer primary so sec stays free. */
    for (int i = 0; i < OPL_4OP_PAIRS; i++) {
        int p = opl_4op_prim[i];
        if (M.opl[p].note < 0)
            return p;
        int s = opl_4op_sec[i];
        if (M.opl[s].note < 0)
            return s;
    }
    /* Steal oldest from the standalone 2-op pool. */
    int victim = opl_2op_only[0];
    uint32_t victim_age = M.opl[victim].age;
    for (int i = 1; i < OPL_2OP_ONLY_COUNT; i++) {
        int ch = opl_2op_only[i];
        if (M.opl[ch].age < victim_age) {
            victim = ch;
            victim_age = M.opl[ch].age;
        }
    }
    kill_channel(victim);
    return victim;
}

/* Compute the OPL3 frequency bytes for a given MIDI note + per-voice
 * semitone offset, honoring the channel's pitch bend (drums skip bend). */
static void compute_play_freq(int midi_ch, int base_note, int note_offset,
                              uint8_t *flo, uint8_t *fhi_blk) {
    int play_note = base_note + note_offset;
    int16_t bend = (midi_ch == 9) ? 0 : M.ch_bend[midi_ch];
    note_to_freq(play_note, bend, flo, fhi_blk);
}

static void opl_note_on(int midi_ch, int note, int vel) {
    const uint8_t *inst;
    if (midi_ch == 9)
        inst = get_drum(note);
    else
        inst = get_instrument(M.ch_program[midi_ch]);

    if (inst_is_blank(inst))
        return;  /* silent patch, nothing to play */

    /* Drums with an explicit percussion_key override use that fixed
     * MIDI note as the base; melodic uses the played note. */
    int base_note;
    if (midi_ch == 9) {
        int pk = inst_percussion_key(inst);
        base_note = pk ? pk : note;
    } else {
        base_note = note;
    }

    int ch_vol = M.ch_volume[midi_ch];

    if (inst_is_pseudo4op(inst)) {
        /* Stacked 2-op voices: independent OPL3 channels, each with
         * its own note offset and FB/CNT. Both halves are tracked in
         * M.opl[] with cross-linked partners so note-off / steal /
         * pitch-bend can find them. */
        int v1 = alloc_2op(midi_ch);
        if (v1 < 0) return;
        /* Mark v1 reserved before allocating v2 so the second alloc
         * doesn't pick the same channel. */
        M.opl[v1].note = (int8_t)note;
        int v2 = alloc_2op(midi_ch);
        if (v2 < 0 || v2 == v1) {
            /* Couldn't get a second slot — fall through and play voice
             * 1 alone rather than dropping the note entirely. */
            v2 = -1;
        }

        load_instrument_2op_at(v1, inst, 4, inst[2]);
        if (v2 >= 0)
            load_instrument_2op_at(v2, inst, 14, inst[3]);

        uint8_t flo1, fhi1;
        compute_play_freq(midi_ch, base_note, inst_note_offset(inst), &flo1, &fhi1);
        uint8_t flo2 = flo1, fhi2 = fhi1;
        if (v2 >= 0)
            compute_play_freq(midi_ch, base_note, inst_note_offset2(inst), &flo2, &fhi2);

        /* Voice-1 state */
        M.opl[v1].midi_ch  = (int8_t)midi_ch;
        M.opl[v1].velocity = (int8_t)vel;
        M.opl[v1].partner  = (int8_t)v2;
        M.opl[v1].op_base  = 4;
        M.opl[v1].age      = M.age_counter++;
        if (v2 >= 0) {
            M.opl[v2].note     = (int8_t)note;
            M.opl[v2].midi_ch  = (int8_t)midi_ch;
            M.opl[v2].velocity = (int8_t)vel;
            M.opl[v2].partner  = (int8_t)v1;
            M.opl[v2].op_base  = 14;
            M.opl[v2].age      = M.age_counter;
        }

        /* Velocity scale each voice with its own KSL_TL bytes. */
        apply_velocity_2op(v1, inst[5],  inst[10], inst[2] & 1, vel, ch_vol);
        if (v2 >= 0)
            apply_velocity_2op(v2, inst[15], inst[20], inst[3] & 1, vel, ch_vol);

        apply_pan(v1, midi_ch);
        if (v2 >= 0)
            apply_pan(v2, midi_ch);

        /* Both voices play independently — both need their own KEY-ON. */
        opl_ch_write(v1, 0xB0, fhi1);
        opl_ch_write(v1, 0xA0, flo1);
        opl_ch_write(v1, 0xB0, 0x20 | fhi1);
        if (v2 >= 0) {
            opl_ch_write(v2, 0xB0, fhi2);
            opl_ch_write(v2, 0xA0, flo2);
            opl_ch_write(v2, 0xB0, 0x20 | fhi2);
        }
        return;
    }

    /* Plain 2-op patch: single OPL3 channel. */
    uint8_t flo, fhi_blk;
    compute_play_freq(midi_ch, base_note, inst_note_offset(inst), &flo, &fhi_blk);
    int prim = alloc_2op(midi_ch);
    if (prim < 0) return;
    load_instrument_2op(prim, inst);

    M.opl[prim].note     = (int8_t)note;
    M.opl[prim].midi_ch  = (int8_t)midi_ch;
    M.opl[prim].velocity = (int8_t)vel;
    M.opl[prim].partner  = -1;
    M.opl[prim].op_base  = 4;
    M.opl[prim].age      = M.age_counter++;

    apply_velocity_2op(prim, inst[5], inst[10], inst[2] & 1, vel, ch_vol);
    apply_pan(prim, midi_ch);

    opl_ch_write(prim, 0xB0, fhi_blk);
    opl_ch_write(prim, 0xA0, flo);
    opl_ch_write(prim, 0xB0, 0x20 | fhi_blk);
}

/* True if this channel is the secondary half of a 4-op fused pair
 * (i.e., the pair's bit is set in register 0x104). Pseudo-4-op stacks
 * use independent 2-op channels and are NOT covered here — both halves
 * own a real KEY-ON. */
static int is_real_4op_secondary(int ch) {
    int pair = ch_to_4op_pair(ch);
    if (pair < 0 || opl_4op_sec[pair] != ch) return 0;
    return (fourop_mask & opl_4op_bit[pair]) != 0;
}

static void opl_note_off(int midi_ch, int note) {
    /* Walk all channels — a stacked-voice patch occupies two channels
     * with the same note/midi_ch; both get released. */
    for (int i = 0; i < OPL_CHANNELS; i++) {
        if (M.opl[i].midi_ch == midi_ch && M.opl[i].note == note) {
            /* In real (fused) 4-op mode the secondary's A0/B0 are
             * ignored — only the primary half writes the KEY-OFF.
             * In pseudo-4-op stacking both halves are independent
             * 2-op voices and BOTH write KEY-OFF. */
            if (!is_real_4op_secondary(i))
                opl_ch_write(i, 0xB0, 0x00);
            M.opl[i].note = -1;
            M.opl[i].partner = -1;
            M.opl[i].age = M.age_counter++;
        }
    }
}

static void all_notes_off(void) {
    for (int i = 0; i < OPL_CHANNELS; i++) {
        opl_ch_write(i, 0xB0, 0x00);
        M.opl[i].note = -1;
        M.opl[i].midi_ch = -1;
        M.opl[i].velocity = 0;
        M.opl[i].partner = -1;
        M.opl[i].op_base = 4;
        M.opl_inst[i] = 0;
    }
    /* Leave all pairs in 2-op mode until a 4-op patch is loaded. */
    if (fourop_mask != 0) {
        fourop_mask = 0;
        of_audio_opl_write(0x104, 0);
    }
}

/* Update carrier TL on all active notes for a MIDI channel after a
 * volume/master change. Each channel is treated independently — for a
 * pseudo-4-op stack both halves get rescaled with their own KSL_TL
 * bytes (op_base picks voice 1 vs voice 2). */
static void update_channel_volume(int midi_ch) {
    for (int i = 0; i < OPL_CHANNELS; i++) {
        if (M.opl[i].midi_ch != midi_ch || M.opl[i].note < 0)
            continue;
        if (is_real_4op_secondary(i))
            continue;  /* primary handles the 4-op pair */
        const uint8_t *inst = M.opl_inst[i];
        if (!inst) continue;
        int ob = M.opl[i].op_base;  /* 4 or 14 */
        if (inst_is_pseudo4op(inst) || ob == 4 || ob == 14) {
            /* mod_kt @ ob+1, car_kt @ ob+6, fb/cnt at inst[2] (v1) or inst[3] (v2). */
            uint8_t fb_byte = (ob == 14) ? inst[3] : inst[2];
            apply_velocity_2op(i, inst[ob + 1], inst[ob + 6], fb_byte & 1,
                               M.opl[i].velocity, M.ch_volume[midi_ch]);
        } else {
            /* Real 4-op (rare): leave the topology-aware path. */
            int partner = M.opl[i].partner;
            apply_velocity(i, partner, inst,
                           M.opl[i].velocity, M.ch_volume[midi_ch], 1);
        }
    }
}

/* Update pitch on all active notes for a MIDI channel after a pitch
 * bend change. Drum channel 9 uses fixed per-patch pitches and ignores
 * bend (matches GM behavior). */
static void update_channel_pitch(int midi_ch) {
    if (midi_ch == 9) return;
    for (int i = 0; i < OPL_CHANNELS; i++) {
        if (M.opl[i].midi_ch != midi_ch || M.opl[i].note < 0)
            continue;
        if (is_real_4op_secondary(i))
            continue;
        const uint8_t *inst = M.opl_inst[i];
        int n_off = 0;
        if (inst) {
            /* op_base=4 → use note_offset1, op_base=14 → note_offset2. */
            n_off = (M.opl[i].op_base == 14)
                ? inst_note_offset2(inst)
                : inst_note_offset(inst);
        }
        int play_note = M.opl[i].note + n_off;
        uint8_t flo, fhi_blk;
        note_to_freq(play_note, M.ch_bend[midi_ch], &flo, &fhi_blk);
        opl_ch_write(i, 0xA0, flo);
        opl_ch_write(i, 0xB0, 0x20 | fhi_blk);
    }
}

/* ======================================================================
 * MIDI event processing
 * ====================================================================== */

/* Process one event from a track. Returns 0 if track ended. */
static int process_event(midi_track_t *t) {
    if (t->done || t->pos >= t->len)
        return 0;

    uint8_t status = t->data[t->pos];
    if (status < 0x80) {
        status = t->running;
    } else {
        t->pos++;
        if (status < 0xF0)
            t->running = status;
    }

    uint8_t cmd = status & 0xF0;
    uint8_t ch  = status & 0x0F;

    if (status == 0xFF) {
        /* Meta event */
        uint8_t meta = t->data[t->pos++];
        uint32_t mlen = read_var(t);

        if (meta == 0x51 && mlen == 3) {
            /* Tempo change */
            M.us_per_beat = ((uint32_t)t->data[t->pos] << 16) |
                            ((uint32_t)t->data[t->pos+1] << 8) |
                            t->data[t->pos+2];
        } else if (meta == 0x2F) {
            t->done = 1;
        }
        t->pos += mlen;
    } else if (status >= 0xF0 && status <= 0xF7) {
        /* SysEx */
        uint32_t slen = read_var(t);
        t->pos += slen;
    } else if (cmd == 0x90) {
        uint8_t note = t->data[t->pos++];
        uint8_t vel  = t->data[t->pos++];
        if (vel > 0)
            opl_note_on(ch, note, vel);
        else
            opl_note_off(ch, note);
    } else if (cmd == 0x80) {
        uint8_t note = t->data[t->pos++];
        t->pos++; /* skip release velocity */
        opl_note_off(ch, note);
    } else if (cmd == 0xC0) {
        M.ch_program[ch] = t->data[t->pos++];
    } else if (cmd == 0xB0) {
        /* Control Change */
        uint8_t cc  = t->data[t->pos++];
        uint8_t val = t->data[t->pos++];
        if (cc == 7) {
            M.ch_volume[ch] = val;
            update_channel_volume(ch);
        } else if (cc == 10) {
            M.ch_pan[ch] = val;
            /* Update pan for active notes on this channel */
            for (int i = 0; i < OPL_CHANNELS; i++)
                if (M.opl[i].midi_ch == ch)
                    apply_pan(i, ch);
        } else if (cc == 123 || cc == 120) {
            /* All Notes Off / All Sound Off */
            for (int i = 0; i < OPL_CHANNELS; i++) {
                if (M.opl[i].midi_ch == ch && M.opl[i].note >= 0) {
                    opl_ch_write(i, 0xB0, 0x00);
                    M.opl[i].note = -1;
                    M.opl[i].age = M.age_counter++;
                }
            }
        }
    } else if (cmd == 0xE0) {
        /* Pitch Bend */
        uint8_t lsb = t->data[t->pos++];
        uint8_t msb = t->data[t->pos++];
        M.ch_bend[ch] = (int16_t)(((uint16_t)msb << 7) | lsb) - 8192;
        update_channel_pitch(ch);
    } else if (cmd == 0xD0) {
        t->pos += 1; /* channel pressure */
    } else if (cmd == 0xA0) {
        t->pos += 2; /* polyphonic key pressure */
    } else {
        t->pos += 2; /* unknown — skip 2 bytes */
    }

    return !t->done;
}

/* Read next delta from track and convert to microseconds */
static void read_next_delta(midi_track_t *t) {
    if (t->done || t->pos >= t->len) {
        t->done = 1;
        return;
    }
    uint32_t delta = read_var(t);
    /* Convert ticks to microseconds: us = delta * us_per_beat / division */
    t->pending_us += (int64_t)delta * M.us_per_beat / M.division;
}

/* ======================================================================
 * MIDI file parsing
 * ====================================================================== */

static int parse_header(void) {
    if (M.len < 14) return OF_MIDI_ERR_BAD_HDR;
    if (M.data[0] != 'M' || M.data[1] != 'T' ||
        M.data[2] != 'h' || M.data[3] != 'd')
        return OF_MIDI_ERR_BAD_HDR;

    uint32_t hdr_len = rd32(M.data + 4);
    M.format = rd16(M.data + 8);
    M.num_tracks = rd16(M.data + 10);
    M.division = rd16(M.data + 12);

    if (M.format > 1) return OF_MIDI_ERR_FORMAT;
    if (M.num_tracks == 0) return OF_MIDI_ERR_NO_TRACKS;
    if (M.num_tracks > MIDI_MAX_TRACKS) M.num_tracks = MIDI_MAX_TRACKS;

    /* Parse track chunks */
    uint32_t offset = 8 + hdr_len;
    int found = 0;
    for (int i = 0; i < M.num_tracks && offset + 8 <= M.len; i++) {
        if (M.data[offset] == 'M' && M.data[offset+1] == 'T' &&
            M.data[offset+2] == 'r' && M.data[offset+3] == 'k') {
            uint32_t tlen = rd32(M.data + offset + 4);
            M.tracks[found].data = M.data + offset + 8;
            M.tracks[found].len = tlen;
            M.tracks[found].pos = 0;
            M.tracks[found].pending_us = 0;
            M.tracks[found].running = 0;
            M.tracks[found].done = 0;
            found++;
            offset += 8 + tlen;
        } else {
            /* Skip unknown chunk */
            uint32_t clen = rd32(M.data + offset + 4);
            offset += 8 + clen;
        }
    }

    M.num_tracks = (uint16_t)found;
    if (found == 0) return OF_MIDI_ERR_NO_TRACKS;

    return OF_MIDI_OK;
}

static void reset_tracks(void) {
    for (int i = 0; i < M.num_tracks; i++) {
        M.tracks[i].pos = 0;
        M.tracks[i].pending_us = 0;
        M.tracks[i].running = 0;
        M.tracks[i].done = 0;
        /* Read initial delta for each track */
        read_next_delta(&M.tracks[i]);
    }
}

static void reset_channels(void) {
    for (int i = 0; i < 16; i++) {
        M.ch_program[i] = 0;
        M.ch_volume[i] = 100;
        M.ch_pan[i] = 64;
        M.ch_bend[i] = 0;
    }
    M.us_per_beat = 500000; /* 120 BPM */
    M.age_counter = 0;
    for (int i = 0; i < OPL_CHANNELS; i++) {
        M.opl[i].note = -1;
        M.opl[i].midi_ch = -1;
        M.opl[i].velocity = 0;
        M.opl[i].partner = -1;
        M.opl[i].op_base = 4;
        M.opl[i].age = 0;
        M.opl_inst[i] = 0;
    }
    fourop_mask = 0;
}

/* ======================================================================
 * Public API
 * ====================================================================== */

int of_midi_init(void) {
    of_audio_opl_reset();

    /* Enable OPL3 mode: register 0x105 bit 0 */
    of_audio_opl_write(0x105, 0x01);

    /* Enable waveform select on BOTH banks. Bank 1's WSE is at 0x101 —
     * without this, OPL3 channels 9-17 ignore waveform select bits in
     * operator registers and fall back to sine waves (organ sound). */
    of_audio_opl_write(0x01, 0x20);
    of_audio_opl_write(0x101, 0x20);

    M.inited = 1;
    M.playing = 0;
    M.paused = 0;
    M.master_volume = 255;
    M.bank = 0;

    reset_channels();
    return OF_MIDI_OK;
}

int of_midi_play(const uint8_t *data, uint32_t len, int loop) {
    if (!M.inited) return OF_MIDI_ERR_NOT_INIT;
    if (M.playing)  return OF_MIDI_ERR_PLAYING;

    M.data = data;
    M.len = len;
    M.looping = loop;

    int rc = parse_header();
    if (rc != OF_MIDI_OK) return rc;

    reset_channels();
    all_notes_off();
    reset_tracks();

    M.playing = 1;
    M.paused = 0;
    M.last_pump_us = clock_us();

    return OF_MIDI_OK;
}

void of_midi_stop(void) {
    all_notes_off();
    M.playing = 0;
    M.paused = 0;
}

void of_midi_pause(void) {
    if (M.playing)
        M.paused = 1;
}

void of_midi_resume(void) {
    if (M.paused) {
        M.paused = 0;
        M.last_pump_us = clock_us();
    }
}

void of_midi_pump(void) {
    if (!M.playing || M.paused)
        return;

    uint32_t now = clock_us();
    int64_t elapsed = (int64_t)(now - M.last_pump_us);
    M.last_pump_us = now;

    /* Clamp elapsed to avoid huge jumps (e.g., after a long pause) */
    if (elapsed > 500000) elapsed = 500000;
    if (elapsed <= 0) return;

    /* Subtract elapsed time from all active tracks */
    for (int i = 0; i < M.num_tracks; i++) {
        if (M.tracks[i].done) continue;
        M.tracks[i].pending_us -= elapsed;
    }

    /* Process events whose time has arrived.
     * Safety limit to avoid infinite loops on malformed files. */
    int safety = 10000;
    int any_active;
    do {
        any_active = 0;
        for (int i = 0; i < M.num_tracks; i++) {
            midi_track_t *t = &M.tracks[i];
            if (t->done) continue;
            any_active = 1;

            while (t->pending_us <= 0 && !t->done && safety > 0) {
                process_event(t);
                if (!t->done)
                    read_next_delta(t);
                safety--;
            }
        }
    } while (0);

    /* Check if all tracks are done */
    if (!any_active) {
        if (M.looping) {
            /* Restart without OPL3 reset for smooth transition */
            all_notes_off();
            reset_channels();
            reset_tracks();
            M.last_pump_us = clock_us();
        } else {
            all_notes_off();
            M.playing = 0;
        }
    }
}

int of_midi_playing(void)  { return M.playing; }
int of_midi_paused(void)   { return M.paused; }
int of_midi_get_volume(void) { return M.master_volume; }

void of_midi_set_volume(int volume) {
    if (volume < 0)   volume = 0;
    if (volume > 255) volume = 255;
    M.master_volume = volume;

    /* Update TL for all currently sounding notes */
    for (int ch = 0; ch < 16; ch++)
        update_channel_volume(ch);
}

void of_midi_load_bank(const uint8_t *bank) {
    M.bank = bank;
    /* Invalidate cached instruments so they get reloaded */
    for (int i = 0; i < OPL_CHANNELS; i++)
        M.opl_inst[i] = 0;
}

const uint8_t *of_midi_builtin_bank(void) {
    /* First 128 × 25 bytes = melodic; callers walk into the drum block
     * via `ptr + 128 * OF_MIDI_INST_SIZE`. gm_bank4 / gm_bank4_drum
     * are stored separately but the accessors in this file present
     * them as one contiguous space when the user supplies a merged
     * bank pointer. Exposing gm_bank4 is enough for the common
     * "overlay melodic patches" use case. */
    return gm_bank4;
}
