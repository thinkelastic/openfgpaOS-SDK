/* ======================================================================
 * OPL3 register mapping for 18 two-operator channels + 6 four-op pairs.
 *
 * Channels 0-8:  bank 0 (registers 0x00-0xFF)
 * Channels 9-17: bank 1 (registers 0x100-0x1FF)
 *
 * 4-op pairs are controlled via register 0x104 (6 bits, one per pair):
 *   bit 0: bank0 ch0+ch3   bit 3: bank1 ch9+ch12
 *   bit 1: bank0 ch1+ch4   bit 4: bank1 ch10+ch13
 *   bit 2: bank0 ch2+ch5   bit 5: bank1 ch11+ch14
 *
 * When a pair's bit is set, the two channels fuse into a single 4-op
 * channel. Primary holds F-num / block / key-on; secondary contributes
 * ops 3-4 and its CNT bit. When the bit is cleared, both channels act
 * as independent 2-op voices.
 * ====================================================================== */

#define OPL_CHANNELS 18

/* Operator register offsets within a bank. ch N uses ops op_mod[N] and
 * op_car[N], each an index into the 0x20/0x40/0x60/0x80/0xE0 blocks. */
static const uint8_t op_mod[9] = { 0, 1, 2, 8, 9, 10, 16, 17, 18 };
static const uint8_t op_car[9] = { 3, 4, 5, 11, 12, 13, 19, 20, 21 };

/* 4-op channel pairs. prim = channel that gets KeyOn/A0/B0; sec =
 * channel that contributes ops 3-4. Both C0 bytes are still written. */
#define OPL_4OP_PAIRS 6
static const uint8_t opl_4op_prim[OPL_4OP_PAIRS] = { 0, 1, 2,  9, 10, 11 };
static const uint8_t opl_4op_sec [OPL_4OP_PAIRS] = { 3, 4, 5, 12, 13, 14 };
static const uint8_t opl_4op_bit [OPL_4OP_PAIRS] = { 0x01, 0x02, 0x04,
                                                     0x08, 0x10, 0x20 };
/* Channels outside any 4-op pair — always usable as plain 2-op voices. */
#define OPL_2OP_ONLY_COUNT 6
static const uint8_t opl_2op_only[OPL_2OP_ONLY_COUNT] = { 6, 7, 8, 15, 16, 17 };

/* Shadow of OPL3 register 0x104. Bit N set → pair N is in 4-op mode. */
static uint8_t fourop_mask;

/* Bank base: 0x000 for channels 0-8, 0x100 for channels 9-17 */
static uint16_t bank_base(int ch) { return (ch >= 9) ? 0x100 : 0x000; }
static int      ch_local(int ch)  { return (ch >= 9) ? (ch - 9) : ch; }

/* Write to a per-channel register (A0/B0/C0) */
static void opl_ch_write(int ch, uint16_t base_reg, uint8_t val) {
    of_audio_opl_write(bank_base(ch) + base_reg + ch_local(ch), val);
}

/* Write to a per-operator register (0x20/0x40/0x60/0x80/0xE0). */
static void opl_op_write(int ch, int is_carrier, uint16_t base_reg, uint8_t val) {
    int lch = ch_local(ch);
    uint8_t off = is_carrier ? op_car[lch] : op_mod[lch];
    of_audio_opl_write(bank_base(ch) + base_reg + off, val);
}

/* Map a channel number to its 4-op pair index, or -1 if it's a
 * standalone 2-op channel (ch 6..8, 15..17). */
static int ch_to_4op_pair(int ch) {
    for (int i = 0; i < OPL_4OP_PAIRS; i++) {
        if (opl_4op_prim[i] == ch || opl_4op_sec[i] == ch)
            return i;
    }
    return -1;
}

/* Enable 4-op mode for the pair that owns the given primary channel. */
static void fourop_enable(int prim) {
    int idx = ch_to_4op_pair(prim);
    if (idx < 0) return;
    uint8_t new_mask = fourop_mask | opl_4op_bit[idx];
    if (new_mask != fourop_mask) {
        fourop_mask = new_mask;
        of_audio_opl_write(0x104, fourop_mask);
    }
}

/* Disable 4-op mode for the pair that contains this channel. Called
 * before loading a 2-op patch on a channel that happens to belong to
 * a 4-op pair, so it reverts to independent 2-op operation. */
static void fourop_disable(int ch) {
    int idx = ch_to_4op_pair(ch);
    if (idx < 0) return;
    uint8_t new_mask = fourop_mask & ~opl_4op_bit[idx];
    if (new_mask != fourop_mask) {
        fourop_mask = new_mask;
        of_audio_opl_write(0x104, fourop_mask);
    }
}

/* F-Numbers for one octave (C4..B4) at block 4.
 * F = note_freq * 2^16 / 49716 (standard OPL3 sample rate). */
static const uint16_t fnums[12] = {
    345, 365, 387, 410, 435, 460,  /* C, C#, D, D#, E, F */
    488, 517, 548, 580, 615, 651   /* F#, G, G#, A, A#, B */
};

