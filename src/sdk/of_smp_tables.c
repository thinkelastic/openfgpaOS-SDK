/* Convert SF2 timecent value to 1 kHz tick count.
 * timecents = 1200 * log2(seconds), so seconds = 2^(tc/1200).
 * ticks = seconds * 1000.
 *
 * Special cases:
 *   tc <= -32768: instantaneous (0 ticks)
 *   tc >= 8000:   clamp to ~110 seconds (110000 ticks)
 */
int32_t smp_timecents_to_ticks(int16_t tc)
{
    if (tc <= -12000) return 0;      /* < 1ms, treat as instant */
    if (tc >= 8000) return 110000;   /* ~110 sec cap */

    /* ticks = 1000 * 2^(tc/1200)
     * = 1000 * 2^(octaves) * cents_to_mult[rem] / 65536  */
    int octaves = tc / 1200;
    int rem = tc - octaves * 1200;
    if (rem < 0) { rem += 1200; octaves--; }

    uint32_t mult = smp_cents_to_mult[rem];
    /* base = 1000 * mult / 65536 = mult * 1000 >> 16 */
    uint64_t base = (uint64_t)mult * 1000;
    int32_t ticks;

    if (octaves >= 0) {
        if (octaves > 16) return 110000;
        ticks = (int32_t)(base >> (16 - octaves));
        if (octaves > 16 || ticks > 110000) ticks = 110000;
    } else {
        int shift = 16 - octaves; /* 16 + |octaves| */
        if (shift >= 48) return 0;
        ticks = (int32_t)(base >> shift);
    }
    if (ticks < 0) ticks = 0;
    return ticks;
}

/* Convert SF2 centibels attenuation to Q16.16 linear level.
 * 0 cB = 1.0 (0x10000), 960 cB = ~0.0001 */
int32_t smp_cb_to_level(int16_t cb)
{
    if (cb <= 0) return 0x10000;
    if (cb >= 960) return 0;
    /* level = 10^(-cB/200)
     * = 2^(-cB * log2(10)/200)
     * = 2^(-cB * 3.32193 / 200)
     * = 2^(-cB * 0.016610)
     * Approximate with lookup: index = cB, 961 entries
     * But simpler: use the cents table with conversion.
     * cB_to_cents = cB * 20 * log2(10) ≈ cB * 33.22
     * Actually, let's just compute it directly. */
    /* Linear approximation: good enough for envelope targets.
     * level = (960 - cB) * 0x10000 / 960 */
    return (int32_t)(960 - cb) * 0x10000 / 960;
}
